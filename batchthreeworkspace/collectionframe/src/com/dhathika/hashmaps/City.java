package com.dhathika.hashmaps;

public class City {
	
	String cityName;
	String cityState;
	String cityCountry;
	Integer cityPin;
	
	public City(String cityName, String cityState, String cityCountry, Integer cityPin) {
		this.cityName = cityName;
		this.cityState = cityState;
		this.cityCountry = cityCountry;
		this.cityPin = cityPin;
	}

}
